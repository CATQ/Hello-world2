		{
			sum = (1500)*0.03 + (3000)*0.1 +(4500)*0.2 +(26000)*0.25+ (n-38500)*0.3;
			
		}		

		if ((n>58500) && (n<=83500))
		{
			sum = (1500)*0.03 + (3000)*0.1 + (4500)*0.2 +(26000)*0.25 + (20000)*0.3 +(n-58500)*0.35;
			
		}
